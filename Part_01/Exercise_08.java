package Part_01;

/**
 * Part 1 Exercise 8:
 *
 *      Write the necessary code that prints out the area
 *      and perimeter of a circle with a radius of 3.14
 *
 *
 */
public class Exercise_08 {


    public static void main(String[] args) {

        double radius = 3.14;

        System.out.println("The area of a circle with a radius of " + radius + " is " + radius*radius + ".");
        System.out.println("The perimeter of a circle with a radius of " + radius + " is " + 2*radius*radius + ".");
    }
}
